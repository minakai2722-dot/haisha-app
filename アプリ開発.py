def load_json(filename, default):
    if os.path.exists(filename):
        with open(filename, 'r', encoding='utf-8') as f:
            return json.load(f)
    return default

def save_json(filename, data):
    with open(filename, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=4)

def get_distance_with_cache(passengers, drivers, target_arrival):
    gmaps = googlemaps.Client(key=API_KEY)
    cache = load_json(CACHE_FILE, {})
    usage = load_json(USAGE_FILE, {"total_elements": 0})

    # ==========================================
    # 1. キャッシュにない「不足している駅」を洗い出す
    # ==========================================
    missing_passengers = []
    missing_drivers = []

    for p in passengers:
        for d in drivers:
            cache_key = f"{p}_{d}"
            if cache_key not in cache:
                # 重複を避けて不足リストに追加
                if p not in missing_passengers: missing_passengers.append(p)
                if d not in missing_drivers: missing_drivers.append(d)

    # ==========================================
    # 2. 不足分がある場合のみ、APIを実行してキャッシュを更新
    # ==========================================
    if missing_passengers and missing_drivers:
        requested_elements = len(missing_passengers) * len(missing_drivers)

        # 使用上限のチェック
        if usage["total_elements"] + requested_elements > MONTHLY_LIMIT:
            raise Exception(f"APIの使用上限に達しました。（今月: {usage['total_elements']} / 上限: {MONTHLY_LIMIT}）")

        print(f"API通信を実行中... (新規 {requested_elements} 要素)")

        # 不足している駅のリストだけでAPIを叩く
        result = gmaps.distance_matrix(
            origins=missing_passengers,
            destinations=missing_drivers,
            mode="transit",
            language="ja",
            arrival_time=target_arrival,
            transit_mode=["bus", "rail"]
        )

        # 結果をパースしてキャッシュ辞書に追加する
        for p_idx, row in enumerate(result['rows']):
            p_station = missing_passengers[p_idx]
            for d_idx, element in enumerate(row['elements']):
                d_station = missing_drivers[d_idx]
                cache_key = f"{p_station}_{d_station}"

                if element['status'] == 'OK':
                    duration_min = element['duration']['value'] // 60
                    cache[cache_key] = duration_min
                else:
                    print(f"警告: {p_station} から {d_station} への経路が見つかりません。")
                    cache[cache_key] = 999  # ペナルティ値もキャッシュに記憶させる

        # 使用量と最新のキャッシュをJSONファイルに書き込んで保存
        usage["total_elements"] += requested_elements
        save_json(USAGE_FILE, usage)
        save_json(CACHE_FILE, cache)
        print("キャッシュデータを更新・保存しました。")
    else:
        print("すべてキャッシュから読み込みました（API通信コストゼロ）。")

    # ==========================================
    # 3. 最適化ソルバー用の2次元配列（行列 d）の生成
    # ==========================================
    d_matrix = []

    # 完全なデータが揃った cache から、元の並び順で行列を作る
    for p in passengers:
        row = []
        for d in drivers:
            cache_key = f"{p}_{d}"
            row.append(cache[cache_key])
        d_matrix.append(row)

    return d_matrix

