def process_form_data(file_path):
    df = pd.read_csv(file_path).dropna(subset=['名前', '最寄り駅'])
    df['名前'] = df['名前'].str.strip()
    df['最寄り駅'] = df['最寄り駅'].str.strip()

    # 運転手と乗客の分割
    df_drivers = df[df['参加形態'] == '運転手']
    df_passengers = df[df['参加形態'] != '運転手']

    drivers = df_drivers['名前'].tolist()
    passengers = df_passengers['名前'].tolist()

    driver_stations = df_drivers['最寄り駅'].tolist()
    passenger_stations = df_passengers['最寄り駅'].tolist()

    # --- 人間関係行列 W の生成 ---
    # 乗客全員のリストから、名前をインデックス（数字）に変換する辞書を作成
  # --- 人間関係行列 W の生成（アップデート版） ---
    p_idx_map = {name: i for i, name in enumerate(passengers)}
    n_passengers = len(passengers)
    W = [[0] * n_passengers for _ in range(n_passengers)]

    # 【ステップ1】誰が誰をどう思っているか（一方向の感情）を記録する辞書
    # 初期値は全員 'NEUTRAL'（なんでもいい）とする
    pref = {i: {j: 'NEUTRAL' for j in range(n_passengers)} for i in range(n_passengers)}

    # スプレッドシートの回答を pref に流し込む
    for _, row in df_passengers.iterrows():
        name = row['名前']
        if name not in p_idx_map: continue
        i = p_idx_map[name]

        # 「一緒になりたい」の上書き
        if pd.notna(row.get('一緒になりたい人')):
            for friend in str(row['一緒になりたい人']).split(','):
                friend = friend.strip()
                if friend in p_idx_map:
                    pref[i][p_idx_map[friend]] = 'WANT'

        # 「気まずい」の上書き（もし同一人物を両方に書いていた場合、気まずいが勝つ設計）
        if pd.notna(row.get('気まずい人')):
            for awkward in str(row['気まずい人']).split(','):
                awkward = awkward.strip()
                if awkward in p_idx_map:
                    pref[i][p_idx_map[awkward]] = 'AWKWARD'

    # 【ステップ2】お互いの感情を突き合わせて、優先度順に最終スコア W を決定する
    # 組み合わせなので、行列の右上半分だけを計算し、左下は対象にコピーする（計算量の削減）
    for i in range(n_passengers):
        for j in range(i + 1, n_passengers):
            p1 = pref[i][j] # iさん から jさん への感情
            p2 = pref[j][i] # jさん から iさん への感情

            # 優先度1：「気まずい」が1人でもいれば、問答無用でペナルティ(100)
            if p1 == 'AWKWARD' or p2 == 'AWKWARD':
                score = 100

            # 優先度2：気まずい人がおらず、どちらか1人でも「一緒がいい」なら報酬(P_SCORE)
            elif p1 == 'WANT' or p2 == 'WANT':
                score = P_SCORE

            # 優先度3：両方とも「なんでもいい」なら影響なし(0)
            else:
                score = 0

            # 対称行列として W にスコアを代入
            W[i][j] = score
            W[j][i] = score

    return passengers, drivers, passenger_stations, driver_stations, W

