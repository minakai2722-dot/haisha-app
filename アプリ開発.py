    C = [5 for _ in range(num_cars)]
    driver_W = [[0] * num_cars for _ in range(num_people)]

    total_available_seats = sum(c - 1 for c in C)

    if num_people > total_available_seats:
        return "【エラー】有効シート数が不足しています。全員を配車することが物理的に不可能です。"

    alpha = 1.0
    beta = 1.0
    lambda_1 = 5000.0
